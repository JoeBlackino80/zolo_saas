export type TranslationKey = keyof typeof TRANSLATIONS.sk;

export function t(key: TranslationKey, locale: Locale = 'sk'): string {
  return (TRANSLATIONS[locale] as Record<string, string>)[key] || TRANSLATIONS.sk[key] || key;
}

export function detectLocale(): Locale {
  if (typeof window === 'undefined') return 'sk';
  const saved = localStorage.getItem('zolo_locale') as Locale;
  if (saved && ['sk', 'cs', 'en'].includes(saved)) return saved;
  const nav = navigator.language?.toLowerCase() || '';
  if (nav.startsWith('cs')) return 'cs';
  if (nav.startsWith('en')) return 'en';
  return 'sk';
}

export function setLocale(locale: Locale) {
  if (typeof window !== 'undefined') {
    localStorage.setItem('zolo_locale', locale);
    document.documentElement.lang = locale;
  }
}
